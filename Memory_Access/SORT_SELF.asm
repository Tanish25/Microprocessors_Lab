#ORG 6500
LXI D, 7001H
MVI C, //DATA IN MEMORY LOCATION IN D



MVI H, 0A // OUTER LOOP COUNTER


OLOOP: 	
	
	MVI L, 0A // INNER LOOP
	DCR H
	JZ EXIT
	ILOOP:
	
		MOV B,C
		INR E
		MOV A,C	
		CMP B
		JC EXCHANGE
		EXCHANGE:
			DCR E
			STAX D
			INR E
			MOV B,A
			STAX D
			JMP CONTINUE
		CONTINUE: 
			DCR L
			JNZ ILOOP
			JMP OLOOP
			
EXIT: 
	HLT
	
		

